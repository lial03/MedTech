// Calculate fractal dimension using box-counting method
export const calculateFractalDimension = (data: number[]): number => {
  if (data.length < 2) return 1.0;
  
  const boxSizes = [2, 4, 8, 16];
  const counts: number[] = [];
  
  for (const boxSize of boxSizes) {
    let count = 0;
    for (let i = 0; i < data.length; i += boxSize) {
      const segment = data.slice(i, i + boxSize);
      if (segment.length > 0) {
        const range = Math.max(...segment) - Math.min(...segment);
        count += Math.ceil(range / boxSize) || 1;
      }
    }
    counts.push(count);
  }
  
  // Linear regression to find slope
  const logBoxSizes = boxSizes.map(s => Math.log(1 / s));
  const logCounts = counts.map(c => Math.log(c));
  
  const n = logBoxSizes.length;
  const sumX = logBoxSizes.reduce((a, b) => a + b, 0);
  const sumY = logCounts.reduce((a, b) => a + b, 0);
  const sumXY = logBoxSizes.reduce((sum, x, i) => sum + x * logCounts[i], 0);
  const sumX2 = logBoxSizes.reduce((sum, x) => sum + x * x, 0);
  
  const slope = (n * sumXY - sumX * sumY) / (n * sumX2 - sumX * sumX);
  
  return Math.abs(slope);
};

// Calculate health score from fractal dimension
export const calculateHealthScore = (sensorData: number[]): number => {
  const fd = calculateFractalDimension(sensorData);
  
  // Normalize fractal dimension to 0-100 scale
  // Healthy signals have FD close to 1.5-1.7
  // Degraded signals have FD < 1.2 or > 2.0
  const idealFD = 1.6;
  const deviation = Math.abs(fd - idealFD);
  const healthScore = Math.max(0, Math.min(100, 100 - (deviation * 100)));
  
  return Math.round(healthScore);
};
