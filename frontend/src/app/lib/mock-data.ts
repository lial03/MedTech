// Generate mock sensor data with fractal properties
const generateSensorData = (baseValue: number, complexity: number): number[] => {
  const data: number[] = [];
  const points = 100;
  
  for (let i = 0; i < points; i++) {
    const noise = Math.sin(i / 5) * complexity + Math.random() * complexity * 0.5;
    const fractalNoise = Math.sin(i / 2) * 2 + Math.sin(i / 10) * 5;
    data.push(baseValue + noise + fractalNoise);
  }
  
  return data;
};

